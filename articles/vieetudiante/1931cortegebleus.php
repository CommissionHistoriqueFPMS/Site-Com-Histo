<p>
    Bleus, vous avez une mère et peut-être des sœurs. Ce ne sont probablement pas là toutes vos relations féminines.
    Eh bien, par respect pour ces êtres qui vous sont chers – car nous espérons bien qu’ils lisent MONS-MINES -,
    nous n’entrerons pas plus dans les détails de cérémonie qui suivit. Si par hasard, il était quelques détails dont
    vous auriez perdu le souvenir, adressez-vous aux anciens. Ils se feront un plaisir de vous rafraîchir la mémoire.
    Nous vous laissons donc ici à votre sort, dont des âmes charitables ayant droit à votre reconnaissance éternelle se
    sont heureusement chargées.
</p>
<p>
    Nous nous excusons auprès des anciens d’avoir adressé ces quelques lignes aux bleu et nous terminons en évoquant
    simplement pour eux les noms magiques du « Celta» de la « Marée» qui seuls dominent, lumine les chants et les ris
    de cette nuit mémorable.
</p>