<p>
    La sortie solennelle du baptême des bleus s’est déroulée le jeudi 5 novembre dernier, revêtue du faste
    et de l’entrain habituels. Parti du Royal sur le coup de six heures, le cortège se dirigea vers l’Ecole
    où le Président Cassart juché sur la statue de Guibal et Devillez ranima dans nos cœurs défaillants
    la reconnaissance que nous devons aux fondateurs de la Faculté, et déposa une gerbe à leurs pieds.
    Il évoqua ensuite la mémoire des étudiants morts au champ d’honneur pendant que l’on déposait
    des fleurs au mémorial, puis les camarades observèrent une émouvante minute de silence au son de
    la Brabanconne et de la Marseillaise.
</p>
<p>
    Et la musique reprit ses accents joyeux pour nous ramener sur la Grand’Place au son des airs familiers.
    Il commençait à faire « soif ». Aussi quelques gosiers à la pente bien marquée exprimèrent-ils leur satifaction en
    arrivant aux « Princes» à labor sympathique, qu’ils ne quittèrent que pour récidiver au « Central» où ils purent
    s’emplir à saaz … iété. Par bonheur. l’on conserva assez de sang-froid pour donner à la cérémonie qui suivit
    l’éclat qui lui était dû. Il ne s’agissait en effet de rien moins que de sacrer étudiant des Mines le singe de la
    Grand’ Garde. Etant donné l’âge vénérable du candidat, les autres bleus lui exprimèrent un profond respect par des
    prosternations adéquates, pendant que le camarade Cassart s’adressait en termes émus au nouvel élu pour lui dire
    combien nous sommes heureux de le compter parmi les nôtres, et termina en lui remettant une blouse de laboratoire
    à sa taille, promettant qu’à la Sainte Barbe il recevrait une penne et une salopette. Le singe qui semble un peu
    dur d’oreilles, prêta la plus grande attention à ces discours et répondit par un mutisme éloquent, la voix mouillée
    d’émotion.
</p>
<p>
    Nous quittâmes notre nouveau camarade et les groupes enfilèrent la rue de la Chaussée pour se préparer à la
    galopade de la Grand’ Rue, au cours de laquelle le gendarme Lebain se rendit compte du danger que l’on rencontre à
    donner le « top » un peu trop tôt. Le cortège se reforma et l’on parti vers la grande salle du Kursaal et atteint
    enfin la gare qui résonna de l’aubade au chef de station chantée à gorge déployée sur les quais par les students
    monôme. Précisément à ce moment arrivait en gare notre sympathique professeur M. Mathieu, rentrant d’un long voyage
    à la colonie. Chargé de nombreux bagages et flanqué de deux solides bamboulas au teint ciragineux, il prit la tête
    du cortège et une petite réception se déroula en son honneur au « Métro ». Doué d’une grande éloquence, encore
    accrue par la connaissaace approfondie des variétés paléontologiques des zônes équatoriales, M. Mathieu voulut
    bien réserver aux étudiants des Mines la primeur de ses importantes découvertes. Il eut d’ailleurs la délicatesse
    de donner aux spécimens les plus remarquables des noms de professeurs de la Faculté, de façon à les perpétuer dans
    la grande famille de notre mère la Science.
</p>
<p>
    A près cette causerie qui constitua le côté scientifique de la soirée eut lieu la réception des délégués venus
    nombreux de Bruxelles, Louvain, Liége, Gand, Lille, Verviers et Gembloux, et auxquels s’étaient joints nos
    camarades du commerce de Mons. Des discours vibrants furent échangés, et l’on partit joyeux pour la dernière étape
    du voyage, celle qui devait hisser les bleus au Paradis terrestre, en l’occurence pour la salle du « Chien Vert ».
</p>
